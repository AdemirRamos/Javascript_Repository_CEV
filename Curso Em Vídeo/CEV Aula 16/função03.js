let v = function (x) { //Nesse caso, temos uma variável sendo uma função.
    return x*2 //Nesse caso, o parâmetro "x" será multiplicado por dois.
}

console.log(v(5)) //À variável foi passado um parâmetro que, por sua vez, ao ser repassado à função, a executou.
